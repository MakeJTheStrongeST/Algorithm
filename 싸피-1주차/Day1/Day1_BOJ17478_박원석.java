package com.ssafy.hw;

import java.util.Scanner;

public class BOJ_17478 {
	static Scanner sc = new Scanner(System.in);
	static int N;
	static StringBuilder sb = new StringBuilder("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.\n");
	
	public static void main(String[] args) {
		N = sc.nextInt();
		
		recursive(0);
		
		System.out.println(sb.toString());
	}
	
	static void recursive(int cnt) {
		sbAppend(cnt, "\"재귀함수가 뭔가요?\"\n");
		
		if(cnt == N) {
			sbAppend(cnt, "\"재귀함수는 자기 자신을 호출하는 함수라네\"\n");
		} else {
			sbAppend(cnt, "\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n");
			sbAppend(cnt, "마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n");
			sbAppend(cnt, "그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n");
			recursive(cnt + 1);
		}
		
		sbAppend(cnt, "라고 답변하였지.\n");
	}
	
	static void sbAppend(int cnt, String str) {
		for(int i = 0; i < 4 * cnt; i++) sb.append("_");
		sb.append(str);
	}
}
